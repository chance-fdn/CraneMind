<template>
  <div class="navbar">
    <hamburger :is-active="sidebar.opened" class="hamburger-container" @toggleClick="toggleSideBar" />

    <breadcrumb class="breadcrumb-container" />

    <div class="right-menu">
      <el-dropdown class="avatar-container" trigger="click">
        <div class="avatar-wrapper">
          <img v-if="avatar" :src="BASE_URL + avatar" class="user-avatar">
          <img v-else src="~@/assets/avatar.png" class="user-avatar">
          <i class="el-icon-caret-bottom" />
        </div>
        <el-dropdown-menu slot="dropdown" class="user-dropdown">
          <el-dropdown-item>
            <span @click="openDialog">用户信息</span>
          </el-dropdown-item>
          <el-dropdown-item divided @click.native="logout">
            <span style="display:block;">退出登录</span>
          </el-dropdown-item>
        </el-dropdown-menu>
      </el-dropdown>
    </div>
    <res-box ref="registerBox" type="amend" />
  </div>
</template>

<script>
import { mapGetters } from 'vuex'
import Breadcrumb from '@/components/Breadcrumb'
import Hamburger from '@/components/Hamburger'
import resBox from '@/components/register'

export default {
  components: {
    Breadcrumb,
    Hamburger,
    resBox
  },
  computed: {
    ...mapGetters([
      'sidebar',
      'avatar'
    ])
  },
  data() {
    return {
      BASE_URL: process.env.VUE_APP_PHOTO_API + 'other/'
    }
  },
  methods: {
    toggleSideBar() {
      this.$store.dispatch('app/toggleSideBar')
    },
    async logout() {
      await this.$store.dispatch('user/logout')
      this.$router.push(`/login?redirect=${this.$route.fullPath}`)
      // this.$router.push(`/login?redirect=/`) // 不保存 退出时访问的页面地址
    },
    openDialog() {
      if (this.$store.getters.name) {
        console.log('----修改----')
        Object.assign(this.$refs.registerBox.registerForm, this.$store.state.user.userInfo)
      }
      this.$refs.registerBox.$refs.dialog.dialogVisible = true
    }
  }
}
</script>

